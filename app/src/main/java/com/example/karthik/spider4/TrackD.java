public class TrackD {
    String name;
    String year;
    String artist;


    public TrackD(String name, String year, String artist) {
        this.name = name;
        this.year = year;
        this.artist = artist;

    }

    public String getName() {
        return name;
    }

    public String getYear() {
        return year;
    }

    public String getArtist() {
        return artist;
    }


}
